WString ToUnicode(const char *src, int l, byte charset)
{
	charset = ResolveCharset(charset);
	if(charset == CHARSET_UTF8)
		return FromUtf8(src, l);
	WStringBuffer result(l);
	ToUnicode(result, src, l, charset);
	return result;
}

